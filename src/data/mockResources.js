export function getResourcesByEvent(eventSlug) {
  return RESOURCES.filter(r => r.eventSlug === eventSlug);
}

export function getResourcesByUser(userId) {
  return RESOURCES.filter(r => r.uploaderId === userId);
}

export function getResourcesByChapter(chapterId) {
  return RESOURCES.filter(r => r.chapterId === chapterId);
}

export function getRecentResources(limit = 10) {
  return [...RESOURCES]
    .sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt))
    .slice(0, limit);
}

export function getPopularResources(limit = 10) {
  return [...RESOURCES]
    .sort((a, b) => b.upvoteCount - a.upvoteCount)
    .slice(0, limit);
}

export function getResourceCountByEvent(eventSlug) {
  return RESOURCES.filter(r => r.eventSlug === eventSlug).length;
}

